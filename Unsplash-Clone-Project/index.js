function display(data) {
	data.forEach(function (ele) {
		let box = document.createElement("div");
		let back = document.createElement("img");
		back.src = ele.back;
		var heart = document.createElement("i");
		heart.setAttribute("class", "fa-solid fa-heart");

		heart.addEventListener("click", function () {
			heart.style.color = "red";
			heart.style.border = "1 px solid red";
		});

		var plus = document.createElement("i");
		plus.setAttribute("class", "fa-solid fa-plus");
		var download = document.createElement("i");
		download.setAttribute("class", "fa-solid fa-download");
		var Hover = document.createElement("div");
		let user = document.createElement("div");
		let pic = document.createElement("img");
		pic.src = ele.pic;
		let name = document.createElement("p");
		name.innerText = ele.name;
		user.append(pic, name);
		Hover.append(plus, heart, download, user);
		box.append(back, Hover);
		main.append(box);
	});
}

